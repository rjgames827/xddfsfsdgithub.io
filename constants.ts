// Fuzzy Link Matching: Normalizes strings to find links even if there are small typos
const normalize = (s: string) => s.toLowerCase().replace(/[^a-z0-9]/g, '');

const findLink = (title: string) => {
  // 1. Direct match
  if (LINK_DB[title]) return LINK_DB[title];
  
  // 2. Fuzzy match (ignore case, spaces, punctuation)
  const normTitle = normalize(title);
  const matchedKey = Object.keys(LINK_DB).find(k => normalize(k) === normTitle);
  
  // 3. Search Fallback
  return matchedKey ? LINK_DB[matchedKey] : getSearchLink(title);
};

const extractYear = (title: string): number | undefined => {
  const match = title.match(/\b(19|20)\d{2}\b/);
  return match ? parseInt(match[0]) : undefined;
};

