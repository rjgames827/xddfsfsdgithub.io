const guessRating = (title: string): number => {
  // Deterministic pseudo-random rating based on title
  let hash = 0;
  for (let i = 0; i < title.length; i++) {
    hash = ((hash << 5) - hash) + title.charCodeAt(i);
    hash |= 0;
  }
  // Map to 6.0 - 9.8 range
  const normalized = Math.abs(hash) % 39; // 0-38
  return Number((6.0 + (normalized / 10)).toFixed(1));
};

